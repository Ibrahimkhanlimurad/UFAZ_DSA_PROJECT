char* morseGenerator(char* str){
    int i,j;
    char texto[55] = {
        'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 
        'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 
        'y', 'z', '1', '2', '3', '4', '5', '6', '7', '8', '9', '0', 
        '.', ',', '?', '!', ';', ':', '/', '-', '\'', '_', '(', ')',
        '"', '=', '+', '*', '@', '&', ' '
     };

    char* encodedText = (char*)calloc(1, 2000); 
    encodedText[0]='\0';

    for(i=0; str[i]!='\0'; i++){
        for(j=0; j<55; j++){
            if(str[i] == texto[j])
                strcat(encodedText, morseEncodeChar(j));
        }
    }

    return encodedText;
}
